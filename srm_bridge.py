# SRMBridge
# ──────────────────────────────────────────────────────────────────────────────

class SRMBridge:
    """
    Converts MCTS planner output into a self-contained JSON decision payload.

    The payload is designed to be:
      1. Passed to a local LLM (Qwen 0.5B) for natural-language narration.
      2. Logged directly as structured data for dashboards / replay.
      3. Human-readable on its own without any additional context.

    Parameters
    ----------
    include_timestamp : bool
        Append a UTC ISO-8601 timestamp to each payload.  Default: True.
    indent : int | None
        JSON indentation spaces.  Use None for compact single-line output
        (saves ~30% bytes when logging at high frequency).  Default: 2.
    """

    def __init__(
        self,
        include_timestamp: bool = True,
        indent:            int | None = 2,
    ) -> None:
        self.include_timestamp = include_timestamp
        self.indent            = indent

    def generate_payload(
        self,
        symbol:          int,
        entropy:         float,
        action:          int,
        q_values:        dict[int, float],
        simulations:     int,
        top_transitions: list[tuple[int, float, int]] | None = None,
        icm_reward:      float | None = None,
        extra:           dict[str, Any] | None = None,
    ) -> str:
        """
        Build and return the decision JSON string.

        Parameters
        ----------
        symbol : int
            Current market symbol ID (from SymbolicQuantizer).
        entropy : float
            Shannon entropy of the current symbol (bits).
        action : int
            Chosen action: 0=HOLD, 1=BUY, 2=SELL.
        q_values : dict[int, float]
            Mapping from action_id to mean reward (Q-value) for each action.
            Example: {0: +0.102, 1: -0.044, 2: +0.031}
        simulations : int
            Number of MCTS simulations run for this decision.
        top_transitions : list of (next_symbol, probability, count) | None
            Top probable next states from MarketTransitionGraph.
            If None, transition reasoning is omitted.
        icm_reward : float | None
            Intrinsic curiosity reward for the last observed transition.
            If None, ICM reasoning is omitted.
        extra : dict | None
            Any additional key-value pairs to append verbatim to the payload.
            Useful for raw_price, tick_index, tape_name etc.

        Returns
        -------
        str : JSON-encoded decision payload.
        """
        band        = _entropy_band(entropy)
        action_name = ACTION_LABELS.get(action, str(action))

        # Best Q-value for the chosen action
        best_q      = q_values.get(action, 0.0)

        # Ranked action table for the payload
        ranked_actions = [
            {
                "action":  ACTION_LABELS.get(a, str(a)),
                "q_value": f"{q:+.4f}",
                "chosen":  (a == action),
            }
            for a, q in sorted(q_values.items(), key=lambda kv: kv[1], reverse=True)
        ]

        # Build reasoning narrative via Python rules (no LLM)
        reasoning = _build_reasoning(
            symbol          = symbol,
            entropy         = entropy,
            action          = action,
            q_values        = q_values,
            simulations     = simulations,
            top_transitions = top_transitions or [],
            icm_reward      = icm_reward,
        )

        # Core payload schema
        payload: dict[str, Any] = {
            "current_symbol":      f"#{symbol:04d}",
            "market_entropy":      f"{band} ({entropy:.2f} bits)",
            "simulations_run":     simulations,
            "decision":            action_name,
            "confidence_q_value":  f"{best_q:+.4f}",
            "action_rankings":     ranked_actions,
            "reasoning":           reasoning,
        }

        # Optional fields
        if top_transitions:
            payload["top_next_states"] = [
                {
                    "symbol":      f"#{t[0]:04d}",
                    "probability": f"{t[1]:.2%}",
                    "obs_count":   t[2],
                }
                for t in top_transitions[:3]
            ]

        if icm_reward is not None:
            payload["icm_curiosity_reward"] = f"{icm_reward:.4f}"

        if self.include_timestamp:
            payload["timestamp_utc"] = datetime.now(timezone.utc).strftime(
                "%Y-%m-%dT%H:%M:%SZ"
            )

        if extra:
            payload.update(extra)

        return json.dumps(payload, indent=self.indent, ensure_ascii=False)

    def generate_from_mcts(
        self,
        planner,          # MCTSPlanner instance
        symbol:     int,
        graph,            # MarketTransitionGraph instance
        icm_reward: float | None = None,
        extra:      dict[str, Any] | None = None,
    ) -> str:
        """
        Convenience wrapper that reads directly from a live MCTSPlanner instance
        after search() has been called.

        Parameters
        ----------
        planner    : MCTSPlanner — must have _last_root populated (post-search).
        symbol     : int          — root symbol that was searched.
        graph      : MarketTransitionGraph — for top_transitions lookup.
        icm_reward : float | None — ICM reward for current tick, if available.
        extra      : dict | None  — verbatim fields to append.

        Returns
        -------
        str : JSON payload
        """
        root = planner._last_root
        if root is None:
            raise RuntimeError("Run planner.search() before calling generate_from_mcts().")

        # Extract Q-values from MCTS root children
        q_values: dict[int, float] = {
            a: node.Q for a, node in root.children.items()
        }

        # Top transitions from graph
        top_transitions = graph.get_probabilities(symbol, top_k=3)

        # Determine best action
        action = max(q_values, key=lambda a: q_values[a]) if q_values else 0

        return self.generate_payload(
            symbol          = symbol,
            entropy         = graph.symbol_entropy(symbol),
            action          = action,
            q_values        = q_values,
            simulations     = planner._last_n_sims,
            top_transitions = top_transitions,
            icm_reward      = icm_reward,
            extra           = extra,
        )

    def slim_payload(self, full_payload: str) -> str:
        """
        Strip verbose fields from a full payload JSON string, returning a
        compact version suitable for the local LLM's limited context window.

        Removed fields:
          * 'reasoning'       — pre-written by Python rules; LLM should derive
                                its own narration from the raw facts.
          * 'action_rankings' — detailed Q-table is redundant for prose rendering.
          * 'timestamp_utc'   — not needed for narration.

        The slim payload averages ~350-400 chars versus ~1100 for the full one,
        cutting token usage by ~65% and staying safely under a 512-token ctx.

        Parameters
        ----------
        full_payload : str
            JSON string from generate_payload() or generate_from_mcts().

        Returns
        -------
        str : compact JSON string with only the essential facts.
        """
        STRIP_FIELDS = {"reasoning", "action_rankings", "timestamp_utc"}
        data = json.loads(full_payload)
        slim = {k: v for k, v in data.items() if k not in STRIP_FIELDS}
        return json.dumps(slim, indent=self.indent, ensure_ascii=False)

    def __repr__(self) -> str:
        return (
            f"SRMBridge("
            f"timestamp={self.include_timestamp}, "
            f"indent={self.indent})"
        )


