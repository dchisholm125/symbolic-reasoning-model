"""
srm_bridge.py

Formats raw MCTS planner output into a strict, minimal JSON payload that can be
handed directly to the local LLM (Qwen 0.5B) for natural-language rendering.

Architecture role
─────────────────
  MCTSPlanner.search()
        │
        ▼
  SRMBridge.generate_payload()   ← pure Python logic, zero model calls
        │
        ▼
  JSON string  ──►  Qwen 0.5B   (renders into human-readable prose)
        │
        └──────►  Logging / Dashboard (raw structured data)

Design principles
──────────────────
  • The JSON schema is intentionally tiny — every field must earn its place.
  • All reasoning strings are generated by deterministic Python rules so the
    LLM's only job is prose rendering, not reasoning. This keeps the LLM
    call trivially fast and prevents hallucination of market facts.
  • The payload is self-contained: a reader with no prior context can
    understand the decision.
  • All numeric fields are pre-formatted as strings so the JSON parses
    identically on any platform (no float precision surprises).
"""

from __future__ import annotations

import json
import time
from datetime import datetime, timezone
from typing import Any

# ──────────────────────────────────────────────────────────────────────────────
# Thresholds (tunable)
# ──────────────────────────────────────────────────────────────────────────────

ENTROPY_HIGH_THRESHOLD   = 5.0   # bits — above this: "HIGH",  MCTS budget large
ENTROPY_MEDIUM_THRESHOLD = 2.0   # bits — above this: "MEDIUM"
                                  #          below:    "LOW"

CONFIDENCE_STRONG  = 0.20        # Q-value difference vs 2nd choice to call "strong"
CONFIDENCE_WEAK    = 0.05        # Below this: "weak"

SELF_LOOP_THRESHOLD = 0.90       # Probability above which a symbol is "trapped"

ACTION_LABELS = {0: "HOLD", 1: "BUY", 2: "SELL"}


# ──────────────────────────────────────────────────────────────────────────────
# Helpers
# ──────────────────────────────────────────────────────────────────────────────

def _entropy_band(entropy: float) -> str:
    if entropy >= ENTROPY_HIGH_THRESHOLD:
        return "HIGH"
    if entropy >= ENTROPY_MEDIUM_THRESHOLD:
        return "MEDIUM"
    return "LOW"


def _confidence_word(q_best: float, q_second: float) -> str:
    """Describe the margin between the best and second-best action."""
    margin = q_best - q_second
    if margin >= CONFIDENCE_STRONG:
        return "strong"
    if margin >= CONFIDENCE_WEAK:
        return "moderate"
    return "weak"


def _build_reasoning(
    symbol:          int,
    entropy:         float,
    action:          int,
    q_values:        dict[int, float],    # {action_id: Q-value}
    simulations:     int,
    top_transitions: list[tuple[int, float, int]],  # [(next_sym, prob, count), …]
    icm_reward:      float | None = None,
) -> str:
    """
    Generate a deterministic, rule-based reasoning narrative from MCTS outputs.

    Rules are applied in order;  relevant fragments are concatenated.
    """
    parts: list[str] = []
    band = _entropy_band(entropy)

    # ── 1. Market state characterisation ──────────────────────────────────────
    if band == "HIGH":
        parts.append(
            f"High-entropy symbol #{symbol:04d} ({entropy:.2f} bits): "
            f"next market state is broadly uncertain across many symbols."
        )
    elif band == "MEDIUM":
        parts.append(
            f"Medium-entropy symbol #{symbol:04d} ({entropy:.2f} bits): "
            f"market shows moderate branching with a few dominant transitions."
        )
    else:
        parts.append(
            f"Low-entropy symbol #{symbol:04d} ({entropy:.2f} bits): "
            f"market is in a near-deterministic regime."
        )

    # ── 2. Transition structure observation ───────────────────────────────────
    if top_transitions:
        top_sym, top_prob, _ = top_transitions[0]
        self_loop = (top_sym == symbol)

        if self_loop and top_prob >= SELF_LOOP_THRESHOLD:
            parts.append(
                f"Dominant self-loop detected: {top_prob:.0%} probability of "
                f"remaining in #{symbol:04d} (stasis regime)."
            )
        elif self_loop:
            parts.append(
                f"Partial self-loop: {top_prob:.0%} chance of staying in "
                f"#{symbol:04d}, remainder disperses to {len(top_transitions)-1} "
                f"successor states."
            )
        else:
            parts.append(
                f"Most likely successor is #{top_sym:04d} ({top_prob:.1%}), "
                f"suggesting a directional market move."
            )

    # ── 3. ICM curiosity signal ────────────────────────────────────────────────
    if icm_reward is not None:
        if icm_reward > 1.5:
            parts.append(
                f"ICM curiosity reward is high ({icm_reward:.3f}): "
                f"this transition is out-of-distribution for the forward model — "
                f"treat this decision with elevated uncertainty."
            )
        elif icm_reward > 0.8:
            parts.append(
                f"ICM curiosity reward is moderate ({icm_reward:.3f}): "
                f"forward model has partial familiarity with this regime."
            )
        else:
            parts.append(
                f"ICM curiosity reward is low ({icm_reward:.3f}): "
                f"forward model is well-calibrated on this state."
            )

    # ── 4. Action selection rationale ─────────────────────────────────────────
    q_sorted = sorted(q_values.items(), key=lambda kv: kv[1], reverse=True)
    best_a, best_q = q_sorted[0]
    label = ACTION_LABELS.get(action, str(action))

    if len(q_sorted) >= 2:
        _, second_q = q_sorted[1]
        conf_word   = _confidence_word(best_q, second_q)
        margin      = best_q - second_q
        parts.append(
            f"MCTS selected {label} with {conf_word} confidence "
            f"(Q={best_q:+.4f}, margin over next-best: {margin:+.4f}) "
            f"across {simulations:,} simulations."
        )
    else:
        parts.append(
            f"MCTS selected {label} (Q={best_q:+.4f}) "
            f"across {simulations:,} simulations."
        )

    # ── 5. Action-specific risk notes ─────────────────────────────────────────
    if action == 1:   # BUY
        if band == "HIGH":
            parts.append(
                "Caution: BUY in a high-entropy regime carries elevated "
                "slippage risk due to wide next-state dispersion."
            )
        else:
            parts.append(
                "BUY signal is consistent with a directional or recovering market."
            )
    elif action == 2:  # SELL
        if band == "LOW":
            parts.append(
                "SELL in a low-entropy (stasis) regime may indicate "
                "mean-reversion opportunity or defensive exit."
            )
        else:
            parts.append(
                "SELL signal reflects the planner's preference to reduce "
                "exposure in the current regime."
            )
    else:              # HOLD
        if band == "HIGH":
            parts.append(
                "HOLD is the risk-minimising choice under high uncertainty: "
                "avoid committing capital until the regime resolves."
            )
        elif top_transitions and top_transitions[0][0] == symbol and \
                top_transitions[0][1] >= SELF_LOOP_THRESHOLD:
            parts.append(
                "HOLD aligns with the stasis regime: "
                "high probability of remaining in current state makes "
                "entering or exiting a position costly."
            )
        else:
            parts.append(
                "HOLD reflects marginal Q-value advantage: "
                "the planner sees no sufficiently rewarded entry/exit opportunity."
            )

    return " ".join(parts)


